a = bool(input("Введите первое значение: "))
b = bool(input("Введите второе значение: "))
z = input("Введите номер логические операции (1. Логическое И; 2. Логическое ИЛИ; 3. Логическое НЕ): ")
c = 0
if z =="1":
        c = a and b
        print (f"Результат: {c}")
elif z =="2":
        c = a or b
        print (f"Результат: {c}")
elif z =="3":
        c = not a
        print (f"Результат: {c}")
        c = not b
        print (f"Результат: {c}")